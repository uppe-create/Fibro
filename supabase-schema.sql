create policy all_cipf_files on cipf_files for all to anon using (true) with check (true);
create policy all_cipf_file_chunks on cipf_file_chunks for all to anon using (true) with check (true);
create policy all_audit_logs on audit_logs for all to anon using (true) with check (true);
